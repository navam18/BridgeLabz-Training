class ProfitAndLoss{
    public static void main(String args[]){
        double cp=129;
        double sp=191;
        double profit=sp-cp;
        double profitPercentage=(profit/cp)*100;
        System.out.println("The Cost Price in INR "+cp+" and Selling Price in INR "+sp+"\nThe Profit in INR is "+profit+" and the profit percentage is "+profitPercentage+"%");
    }
}